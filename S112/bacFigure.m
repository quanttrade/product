function h = bacFigure(y_re,tref,t_str1,leg_str,new_sel,log_trans)
    if nargin <6
        log_trans=0;
    end
    if nargin < 5
        new_sel=1;
    end
    if new_sel==1
        h = figure;
    else
        h=[];
    end
    
    colororder = [0.00  0.00  1.00;...
	0.00  0.50  0.00;...
	1.00  0.00  0.00;...
	0.00  0.75  0.75;...
	0.75  0.00  0.75;...
	0.75  0.75  0.00;...
	0.25  0.25  0.25;...
	0.75  0.25  0.25;...
	0.95  0.95  0.00;... 
	0.25  0.25  0.75;...
	0.75  0.75  0.75;...
	0.00  1.00  0.00;... 
	0.76  0.57  0.17;...
	0.54  0.63  0.22;...
	0.34  0.57  0.92;...
	1.00  0.10  0.60;...
	0.88  0.75  0.73;...
	0.10  0.49  0.47;...
	0.66  0.34  0.65;...
	0.99  0.41  0.23];

    
    t_str = cellfun(@(x) strjoin(strsplit(x,'-'),''),tref,'UniformOutput',false);
    T = length(t_str);
    set(gca, 'ColorOrder', colororder);
    if eq(log_trans,0)
        hold on
        plot(y_re,'LineWidth',2);
    else
        semilogy(y_re,'LineWidth',2);
    end
    set(gca,'xlim',[0,T]);
    set(gca,'XTick',floor(linspace(1,T,15)));
    set(gca,'XTickLabel',t_str(floor(linspace(1,T,15))));
    set(gca,'XTickLabelRotation',90)    
    setpixelposition(h,[223,365,1345,420]);
    %legend(leg_str,'Location','best')
    box off
    if ~isempty(t_str1)
        t_str1 = strrep(t_str1,'_','-');
        title(t_str1)
    end
    if ~isempty(leg_str)
        leg_str = cellfun(@(x) strrep(x,'_','-'),leg_str,'UniformOutput',false);
        legend(leg_str,'location','best')
    end
end